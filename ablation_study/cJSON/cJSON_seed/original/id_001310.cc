int test_cJSON_api_sequence() {
    // step 1: Initialize
    cJSON *root = cJSON_CreateObject();
    cJSON *child = cJSON_AddObjectToObject(root, "settings");
    cJSON *true_item = cJSON_CreateTrue();
    cJSON_AddItemToObject(child, "enabled", true_item);

    // step 2: Configure
    cJSON *false_item = cJSON_AddFalseToObject(child, "disabled");
    cJSON *subobj = cJSON_AddObjectToObject(child, "subsettings");
    cJSON_AddStringToObject(subobj, "mode", "test_mode");

    // step 3: Operate
    char *printed = cJSON_PrintBuffered(root, 256, 1);

    // step 4: Validate & Cleanup
    size_t printed_len = strlen(printed);
    char *buf = (char *)cJSON_malloc(printed_len + 1);
    memset(buf, 0, printed_len + 1);
    memcpy(buf, printed, printed_len + 1);
    cJSON_Minify(buf);
    cJSON_free(printed);
    cJSON_free(buf);
    cJSON_Delete(root);
    // API sequence test completed successfully
    return 66;
}