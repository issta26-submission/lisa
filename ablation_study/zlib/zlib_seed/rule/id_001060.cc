int test_zlib_api_sequence() {
    // step 1: Declarations & prepare buffers
    const uLong ORIG_LEN = 512UL;
    Bytef *orig = (Bytef *)malloc((size_t)ORIG_LEN);
    memset(orig, 'Z', (size_t)ORIG_LEN);
    const char *ver = zlibVersion();
    uLong bound = compressBound(ORIG_LEN);
    Bytef *comp = (Bytef *)malloc((size_t)bound);
    memset(comp, 0, (size_t)bound);

    // step 2: Initialize and perform compression (Initialize -> Operate)
    z_stream dstrm;
    memset(&dstrm, 0, sizeof(dstrm));
    dstrm.zalloc = NULL;
    dstrm.zfree = NULL;
    dstrm.opaque = NULL;
    deflateInit_(&dstrm, 6, ver, (int)sizeof(z_stream));
    dstrm.next_in = orig;
    dstrm.avail_in = (uInt)ORIG_LEN;
    dstrm.next_out = comp;
    dstrm.avail_out = (uInt)bound;
    deflate(&dstrm, 4);
    uLong comp_size = dstrm.total_out;
    deflateEnd(&dstrm);

    // step 3: Initialize inflate and configure dictionary (Configure -> Validate)
    z_stream istrm;
    memset(&istrm, 0, sizeof(istrm));
    istrm.zalloc = NULL;
    istrm.zfree = NULL;
    istrm.opaque = NULL;
    inflateInit_(&istrm, ver, (int)sizeof(z_stream));
    inflateSetDictionary(&istrm, orig, (uInt)64);

    // step 4: Write outputs to gzip handles and cleanup (Operate -> Cleanup)
    gzFile gf = gzopen("zlib_api_sequence_tmp.gz", "wb");
    gzwrite(gf, comp, (unsigned int)comp_size);
    gzclose(gf);
    gzFile gout = gzdopen(1, "wb");
    gzwrite(gout, orig, (unsigned int)ORIG_LEN);
    gzclose(gout);
    inflateEnd(&istrm);
    free(orig);
    free(comp);
    (void)ver;
    (void)bound;
    (void)comp_size;
    // API sequence test completed successfully
    return 66;
}