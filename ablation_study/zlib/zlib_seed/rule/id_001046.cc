int test_zlib_api_sequence() {
    // step 1: Declarations & prepare buffers
    const uLong ORIG_LEN = 256UL;
    Bytef *orig = (Bytef *)malloc((size_t)ORIG_LEN);
    memset(orig, 'A', (size_t)ORIG_LEN);
    uLong flags = zlibCompileFlags();
    uLong bound = compressBound(ORIG_LEN);
    Bytef *comp = (Bytef *)malloc((size_t)bound);
    memset(comp, 0, (size_t)bound);
    Bytef *decomp = (Bytef *)malloc((size_t)ORIG_LEN);
    memset(decomp, 0, (size_t)ORIG_LEN);

    // step 2: Initialize deflate, reset, perform compression, and cleanup deflate
    z_stream dstrm;
    memset(&dstrm, 0, sizeof(dstrm));
    dstrm.zalloc = NULL;
    dstrm.zfree = NULL;
    dstrm.opaque = NULL;
    deflateInit_(&dstrm, 6, zlibVersion(), (int)sizeof(z_stream));
    dstrm.next_in = orig;
    dstrm.avail_in = (uInt)ORIG_LEN;
    dstrm.next_out = comp;
    dstrm.avail_out = (uInt)bound;
    deflateReset(&dstrm);
    deflate(&dstrm, 4); /* Z_FINISH */
    uLong comp_size = dstrm.total_out;
    deflateEnd(&dstrm);

    // step 3: Write original data to a gzip file using gzbuffer and gzputc, then close
    gzFile gw = gzopen("zlib_api_sequence_tmp.gz", "wb");
    gzbuffer(gw, 8192);
    gzputc(gw, (int)orig[0]);
    gzwrite(gw, orig + 1, (unsigned int)(ORIG_LEN - 1));
    gzclose(gw);

    // step 4: Initialize inflate, perform inflation to validate, cleanup, and final free
    z_stream istrm;
    memset(&istrm, 0, sizeof(istrm));
    istrm.zalloc = NULL;
    istrm.zfree = NULL;
    istrm.opaque = NULL;
    inflateInit_(&istrm, zlibVersion(), (int)sizeof(z_stream));
    istrm.next_in = comp;
    istrm.avail_in = (uInt)comp_size;
    istrm.next_out = decomp;
    istrm.avail_out = (uInt)ORIG_LEN;
    inflate(&istrm, 0); /* Z_NO_FLUSH */
    inflateEnd(&istrm);

    free(orig);
    free(comp);
    free(decomp);
    (void)flags;
    (void)comp_size;
    // API sequence test completed successfully
    return 66;
}