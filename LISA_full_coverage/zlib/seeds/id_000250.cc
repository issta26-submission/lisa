int test_zlib_api_sequence() {
    // Step 1: Declarations
    z_stream def_strm;
    z_stream inf_strm;
    gzFile gz_file = Z_NULL;
    const char *temp_filename = "zlib_prime_uncompress_test.gz";

    Bytef source_data[128]; // Buffer for uncompress source
    uLong source_len = sizeof(source_data);

    Bytef dest_data[256]; // Buffer for uncompress destination
    uLongf dest_len = sizeof(dest_data);

    char read_buf[64]; // Buffer for gzread and gzfread
    int ret;

    // Step 2: Setup and Initialization
    memset(source_data, 'X', source_len); // Fill source data for potential uncompress call
    memset(dest_data, 0, dest_len);
    memset(read_buf, 0, sizeof(read_buf));

    // Initialize deflate stream for deflatePrime
    def_strm.zalloc = Z_NULL;
    def_strm.zfree = Z_NULL;
    def_strm.opaque = Z_NULL;
    ret = deflateInit_(&def_strm, Z_DEFAULT_COMPRESSION, ZLIB_VERSION, (int)sizeof(z_stream));

    // Initialize inflate stream for inflatePrime and inflateEnd
    inf_strm.zalloc = Z_NULL;
    inf_strm.zfree = Z_NULL;
    inf_strm.opaque = Z_NULL;
    ret = inflateInit_(&inf_strm, ZLIB_VERSION, (int)sizeof(z_stream));

    // Step 3: Core Operations - DeflatePrime and GZ File Write
    // Prime the deflate stream with some bits and a value.
    ret = deflatePrime(&def_strm, 4, 0x0A);

    // Open a gzipped file for writing.
    gz_file = gzopen(temp_filename, "wb");

    // Write individual characters to the gzipped file.
    gzputc(gz_file, 'Z');
    gzputc(gz_file, 'l');
    gzputc(gz_file, 'i');
    gzputc(gz_file, 'b');
    gzputc(gz_file, '\n');
    // Edge Case 1: Call gzputc with an invalid character value (e.g., EOF or -1).
    // This tests how the function handles out-of-range integer inputs.
    gzputc(gz_file, -1);
    ret = gzclose(gz_file); // Close the file after writing

    // Step 4: Core Operations - GZ File Read
    // Reopen the gzipped file for reading.
    gz_file = gzopen(temp_filename, "rb");

    // Read a block of data using gzread.
    ret = gzread(gz_file, read_buf, 10);

    // Read items using gzfread.
    z_size_t items_read = gzfread(read_buf, 1, 5, gz_file);

    // Edge Case 2: Call gzread with zero length.
    // This tests how gzread handles requests for no data, which should typically return 0.
    ret = gzread(gz_file, read_buf, 0);

    // Step 5: Core Operations - InflatePrime and Uncompress
    // Prime the inflate stream with some bits and a value.
    ret = inflatePrime(&inf_strm, 6, 0x3F);

    // Edge Case 3: Call uncompress with zero source length and a NULL source buffer.
    // This simulates an empty or invalid compressed input for robustness testing.
    uLongf zero_source_len = 0;
    uLongf dummy_dest_len = sizeof(dest_data);
    ret = uncompress(dest_data, &dummy_dest_len, Z_NULL, zero_source_len);

    // Call uncompress with a dummy source and destination buffer.
    // This call is for demonstrating API usage; it will likely return Z_DATA_ERROR
    // as source_data is not valid compressed data generated by zlib.
    uLongf actual_dest_len = sizeof(dest_data);
    ret = uncompress(dest_data, &actual_dest_len, source_data, source_len);

    // Step 6: Cleanup
    ret = gzclose(gz_file); // Close the file opened for reading
    ret = inflateEnd(&inf_strm); // Clean up the inflate stream resources
    ret = deflateEnd(&def_strm); // Clean up the deflate stream resources
    remove(temp_filename); // Remove the temporary file created during the test

    printf("API sequence test completed successfully\n");

    return 66;
}